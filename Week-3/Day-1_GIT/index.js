/*

GIT adalah version control atau suatu proses  dimana setiap  data yang kita buat, 
ketika kita melakukan perubahan akan di simpan tanpa menimpa data pertama/master nya dan 
GIT memudahkan kita dalam nge-Track data. didalam pembuatan GIT terdapat  4 tahap   penyimpanan data/folder yaitu: 
1. working directory ialah directory untuk menyimpan kode belum selesai atau dalam pengerjaan. 
2. staging area ialah memastikan perubahan benar-benar sudah tepat yaitu kode/folder yang kita telah buat. 
3. local repository yaitu perubahan yang kita lakukan hanya terjadi di komputer dan orang lain tidak dapat mengaksesnya/melihatnya. 
4. remote repository dimana perubahan  code/repository dapat  diakses orang lain. setelah tahu tentang 4 tahap diatas, 

git init yaitu untuk memberitahu bahwa code/folder kita dalam tahap working repository. 
git add untuk membuat dari tahap working repository ke staging area. 
git remote add oringin "link github" yaitu memberitahu bahwa akan membuat dari local ke remote repository . 
git push origin <nama branch> membuat membuat local repository ke remote repository. 
git checkout <nama branch> berpindah ke suatu branch, git status melihat status. 
git remote-v untuk tahu apakah yang sudah di commit. 
git merge menggabung suatu branch. 
git clone <link> untuk clone repository. 
git commit -m "pesan perubahan apa" untuk memberikan pesan perubahan apa yang telah dilakukan.

*/
